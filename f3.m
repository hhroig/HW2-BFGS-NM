function [f,g,H] = f3(x)
% f_3 (x) = exp(x1 + 3x2 - 0.1) + exp(x1 - 3x2 -0.1) + exp(-x1 -0.1)
% x = (x_1, x_2) in R^2 
f = exp(x(1) + 3*x(2) - 0.1) + exp(x(1) - 3*x(2) - 0.1) + exp(-x(1) -0.1);

g = [exp(x(1) + 3*x(2) - 0.1) + exp(x(1) - 3*x(2) - 0.1) - exp(-x(1) -0.1);...
    3*exp(x(1) + 3*x(2) - 0.1) - 3*exp(x(1) - 3*x(2) - 0.1)];

H = [exp(x(1) + 3*x(2) - 0.1) + exp(x(1) - 3*x(2) - 0.1) + exp(-x(1) -0.1),...
    3*exp(x(1) + 3*x(2) - 0.1) - 3*exp(x(1) - 3*x(2) - 0.1);...
    3*exp(x(1) + 3*x(2) - 0.1) - 3*exp(x(1) - 3*x(2) - 0.1),...
    9*exp(x(1) + 3*x(2) - 0.1) + 9*exp(x(1) - 3*x(2) - 0.1)];
end
