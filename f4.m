function [f,g,H] = f4(x)
% f_4 (x) = 100*(x(2) - x(1)^2)^2 + (1-x(1))^2
% x = (x_1, x_2) in R^2 
f =  100*(x(2) - x(1)^2)^2 + (1-x(1))^2;

g = [400*( x(1)^3 - x(2)*x(1) ) + 2*x(1) - 2;...
    200*x(2) - 200*x(1)^2];

H = [400*( 3*x(1)^2 - x(2) ) + 2, -400*x(1);...
    -400*x(1), 200];
end
